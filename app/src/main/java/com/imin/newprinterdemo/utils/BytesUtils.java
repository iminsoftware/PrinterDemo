    public static byte[] getCodePageTypeCommand(String codePageType, int codePosition, int inch) {
        EscCommand escCommand = new EscCommand();
        escCommand.addInitializePrinter();
        escCommand.addText(codePageType + "\n");
        escCommand.addCancelKanjiMode();
        byte[] command = {27, 116, (byte) codePosition};
        escCommand.addUserCommand(command);
        ByteArrayOutputStream byteArrayOutputStream = new ByteArrayOutputStream();
        escCommand.addText("FontA\n");
        escCommand.addSelectCharacterFont(EscCommand.FONT.FONTA);
        escCommand.addUserCommand(inch == Inch.PRINT_HEAD_INCH_80 ? "   " : "");
        for (int i = 0; i < 16; i++) {
            escCommand.addUserCommand(String.format("%01X ", Integer.valueOf(i)));
        }
        escCommand.addPrintAndLineFeed();
        escCommand.addUserCommand(inch == Inch.PRINT_HEAD_INCH_80 ? String.format("%01X ", 32) : "");
        for (int i2 = 32; i2 < 256; i2++) {
            if (i2 != 32 && i2 % 16 == 0) {
                escCommand.addUserCommand(byteArrayOutputStream.toByteArray());
                byteArrayOutputStream.reset();
                escCommand.addUserCommand("\r\n");
                escCommand.addUserCommand(inch == Inch.PRINT_HEAD_INCH_80 ? String.format("%01X ", Integer.valueOf(i2)) : "");
            }
            if (i2 != 10) {
                byteArrayOutputStream.write(i2);
                byteArrayOutputStream.write(32);
            }
        }
        escCommand.addUserCommand(byteArrayOutputStream.toByteArray());
        byteArrayOutputStream.reset();
        escCommand.addPrintAndLineFeed();
        escCommand.addText("FontB\n");
        escCommand.addSelectCharacterFont(EscCommand.FONT.FONTB);
        escCommand.addUserCommand("   ");
        for (int i3 = 0; i3 < 16; i3++) {
            escCommand.addUserCommand(String.format("%01X ", Integer.valueOf(i3)));
        }
        escCommand.addPrintAndLineFeed();
        escCommand.addUserCommand(String.format("%01X ", 32));
        for (int i4 = 32; i4 < 256; i4++) {
            if (i4 != 32 && i4 % 16 == 0) {
                escCommand.addUserCommand(byteArrayOutputStream.toByteArray());
                byteArrayOutputStream.reset();
                escCommand.addUserCommand("\r\n");
                escCommand.addUserCommand(String.format("%01X ", Integer.valueOf(i4)));
            }
            if (i4 != 10) {
                byteArrayOutputStream.write(i4);
                byteArrayOutputStream.write(32);
            }
        }
        escCommand.addUserCommand(byteArrayOutputStream.toByteArray());
        byteArrayOutputStream.reset();
        escCommand.addSelectKanjiMode();
        escCommand.addPrintAndLineFeed();
        escCommand.addPrintAndLineFeed();
        escCommand.addCutAndFeedPaper((byte) 0);
        return escCommand.getCommand();
    }

